    private void addMulProxy(java.awt.event.ActionEvent evt)//GEN-FIRST:event_addMulProxy
    {//GEN-HEADEREND:event_addMulProxy
        String begIpString;
        String endIpString;
        int port = 0;
        Proxy.Type type;
        
        //获取起始IP
        begIpString = begipTextField.getText();
        if (!InetAddressHelper.isVaildAddress(begIpString))
        {
            JOptionPane.showMessageDialog(this, "非法起始IP", "非法起始IP "
                                            ,JOptionPane.WARNING_MESSAGE); 
        }
        //获取终止IP
        endIpString = endipTextField.getText();
        if (!InetAddressHelper.isVaildAddress(endIpString))
        {
            JOptionPane.showMessageDialog(this, "非法终止IP", "非法终止IP "
                                            ,JOptionPane.WARNING_MESSAGE); 
        }
        //获取端口
        try
        {
           port = Integer.valueOf(portTextField.getText());
            if (port < 0 || port > 65535 )
            {
                throw new NumberFormatException();
            }
        } catch (NumberFormatException e)
        {
            JOptionPane.showMessageDialog(this, "非法Port", "非法Port "
                                            ,JOptionPane.WARNING_MESSAGE); 
        }
        //获取类型
        type = Proxy.Type.valueOf(typeComboBox.getSelectedItem().toString());
        //添加代理
        Application.proxyPool.add(begIpString,endIpString, port, type);
        //关闭对话框
        dispose();
    }//GEN-LAST:event_addMulProxy

